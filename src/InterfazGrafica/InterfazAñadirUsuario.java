    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String nombre=txtnombre.getText();
        String usuario=txtusuario.getText();
        String contraseña=txtcontraseña.getText();
        String contraseña2=txtcontraseña2.getText();
        if(contraseña2.equals(contraseña)){
            if (contraseña.length()>=8){
                usuarios usu=new usuarios(1,nombre,usuario,contraseña);
                usu.registrar();
                txtnombre.setText("");
                txtusuario.setText("");
                txtcontraseña.setText("");
                txtcontraseña2.setText("");
                this.setVisible(false);
            }else{
                JOptionPane.showMessageDialog(null, "La contraseña debe tener minimo 8 caracteres");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Las contraseñas no coinciden");
        }
    }//GEN-LAST:event_jButton1ActionPerformed


